#include <iostream> // Include the input/output library

using namespace std;

int main() {
    // Step 1: Declare and initialize the first integer variable
    int num1 = 8;

    // Step 2: Declare and initialize the second integer variable
    int num2 = 10;

    // Step 3: Declare a third variable that adds the previous two
    int sum = num1 + num2;

    // Step 4: Print the value of the sum to the console
    cout << "The sum is " << sum << endl;

    return 0; // End of program
}
